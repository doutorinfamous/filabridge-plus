func TestResolveFilamentUsageGcodeTakesPriorityOverMetadata(t *testing.T) {
	content := []byte(`; filament used [g] = 9.15`)
	metadata := &FilamentUsageMetadata{
		FilamentWeightTotal: 99.0,
		ReferencedTools:     []int{1},
	}

	resolution := ResolveFilamentUsage(content, metadata)
	if resolution.Source != "gcode_single_extruder" {
		t.Fatalf("expected gcode to win, got source %s", resolution.Source)
	}
	if resolution.Usage[0] != 9.15 {
		t.Fatalf("expected extruder 0 from gcode, got %v", resolution.Usage)
	}
}

