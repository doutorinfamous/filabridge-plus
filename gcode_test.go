package main

import "testing"

func TestParseGcodeFilamentUsageCommaSeparated(t *testing.T) {
	content := []byte(`; generated by OrcaSlicer
; filament used [g] = 12.34, 5.67
M104 S200`)

	usage, err := ParseGcodeFilamentUsage(content)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	if usage[0] != 12.34 {
		t.Fatalf("expected toolhead 0 to use 12.34g, got %v", usage[0])
	}
	if usage[1] != 5.67 {
		t.Fatalf("expected toolhead 1 to use 5.67g, got %v", usage[1])
	}
}

func TestParseGcodeFilamentUsageOrcaExtruder1(t *testing.T) {
	content := []byte(`; generated by OrcaSlicer
; filament used [g] = 0, 9.15, 0, 0`)

	resolution := ResolveFilamentUsage(content, nil)
	if resolution.Source != "gcode_comma" {
		t.Fatalf("expected source gcode_comma, got %s", resolution.Source)
	}
	if resolution.Usage[1] != 9.15 {
		t.Fatalf("expected extruder 1 to use 9.15g, got %v", resolution.Usage)
	}
	if _, ok := resolution.Usage[0]; ok {
		t.Fatal("extruder 0 should not have usage")
	}
}

